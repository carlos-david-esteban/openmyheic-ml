/**
 * generate-blog-manifest.js — builds src/lib/blogManifest.generated.ts
 *
 * Reads seo/content/*.md (EN, root) and seo/content/<lang>/*.md (localized)
 * and produces a small typed manifest (slug, title, description, date, path)
 * per language. This file is imported BOTH by the React SPA (bundled by
 * vite, for the "From the blog" home section) and by seo-scripts/prerender.js
 * (via esbuild, same trick already used for src/lib/seo.ts) so the SPA and
 * the prerendered HTML always show the exact same list.
 *
 * Must run BEFORE `vite build` (it writes into src/lib), which itself runs
 * BEFORE the blog markdown->HTML engine (seo-scripts/build.js) in the
 * Cloudflare Pages build command. This script only *reads* seo/content/*.md,
 * it never touches dist/, so the ordering vs. the blog engine doesn't matter.
 *
 * Usage: node seo-scripts/generate-blog-manifest.js   (cwd = app/frontend)
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(__dirname, "..");
const contentDir = path.join(projectRoot, "seo", "content");
const outFile = path.join(projectRoot, "src", "lib", "blogManifest.generated.ts");

// Mirrors SUPPORTED_POST_LANGS in seo-scripts/convert-blog-to-html.js
const SUPPORTED_POST_LANGS = ["de", "ja", "es", "fr", "pt", "ko", "nl", "sv", "no", "da"];

function parseFrontmatterScalars(raw) {
  const m = raw.match(/^---\s*\n([\s\S]*?)\n---\s*\n/);
  const fm = {};
  if (!m) return fm;
  m[1].split("\n").forEach((line) => {
    // Only top-level "key: value" lines (no leading whitespace) — skips
    // indented YAML list items like "  - HEIC" under tags:.
    const mm = line.match(/^([a-zA-Z]+):\s?(.*)$/);
    if (!mm) return;
    let val = mm[2].trim();
    if (
      (val.startsWith('"') && val.endsWith('"')) ||
      (val.startsWith("'") && val.endsWith("'"))
    ) {
      val = val.slice(1, -1);
    }
    if (val !== "") fm[mm[1]] = val;
  });
  return fm;
}

function collectPostsForLang(lang) {
  const dir = lang === "en" ? contentDir : path.join(contentDir, lang);
  if (!fs.existsSync(dir)) return [];
  const files = fs.readdirSync(dir).filter((f) => f.endsWith(".md"));
  const prefix = lang === "en" ? "" : `/${lang}`;
  const posts = files.map((f) => {
    const raw = fs.readFileSync(path.join(dir, f), "utf-8");
    const fm = parseFrontmatterScalars(raw);
    const slug = f.replace(/\.md$/, "");
    return {
      slug,
      title: fm.title || slug,
      description: fm.description || "",
      date: fm.date || "",
      path: `${prefix}/blog/${slug}/`,
    };
  });
  posts.sort((a, b) => {
    const da = a.date ? new Date(a.date).getTime() : 0;
    const db = b.date ? new Date(b.date).getTime() : 0;
    return db - da;
  });
  return posts;
}

const manifest = {};
manifest.en = collectPostsForLang("en");
for (const lang of SUPPORTED_POST_LANGS) {
  manifest[lang] = collectPostsForLang(lang);
}

const totalPosts = Object.values(manifest).reduce((n, arr) => n + arr.length, 0);

const banner = `// AUTO-GENERATED by seo-scripts/generate-blog-manifest.js — do not edit by hand.
// Regenerated on every build (npm run build) from seo/content/*.md frontmatter.
// Used by src/components/BlogSection.tsx (SPA) and seo-scripts/prerender.js (SSG)
// so the "From the blog" home section stays in sync in both places.

export interface BlogManifestPost {
  slug: string;
  title: string;
  description: string;
  date: string;
  path: string;
}

export const blogManifest: Record<string, BlogManifestPost[]> = ${JSON.stringify(manifest, null, 2)};
`;

fs.mkdirSync(path.dirname(outFile), { recursive: true });
fs.writeFileSync(outFile, banner, "utf-8");

console.log(`[generate-blog-manifest] wrote ${outFile} — ${totalPosts} posts across ${Object.keys(manifest).length} languages`);
